namespace App\Domain\Entity\Traits;

use App\Domain\Entity\Auth;

/**
 * HasAuth
 *
 * @author Fathalfath30
 * @version 1.0.0
 * @since 1.0.0
 *
 * @see \App\Domain\Entity\Auth
 */
trait HasAuth {
  const AUTH = 'auth';

  /** @var \App\Domain\Entity\Auth $auth */
  private Auth $auth;


  /**
   * @return \App\Domain\Entity\Auth
   */
  public function getAuth() : Auth {
    return $this->auth;
  }
}
